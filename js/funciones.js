var mostrar_alias=function() {

    var elemento1 = document.getElementById('label_usu_alias');
    var elemento2 = document.getElementById('usu_alias');

     if (elemento1.disabled == true) {
        elemento1.disabled = false;
    } else {
        elemento1.disabled = true;
    }

	if (elemento2.disabled == true) {
	        elemento2.disabled = false;
	    } else {
	        elemento2.disabled = true ;
	    }
}

